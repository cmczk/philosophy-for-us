    <h2>Как писать материалы?</h2>
  </div>

  <div class="how-to-write-section-body">
    <ol class="rules-list">
      <li class="rules-item">
        Материал, который вы присылаете, должен быть написан вами самостоятельно. Желательно, чтобы
        темы, на которую вы пишете, мы для вас интересна и личностно близка: вы делитесь этим с
        другими людьми, которых может заинтересовать та же идея, мысль, цитата, книга,
        биографический факт и т. д.
      </li>
      <li class="rules-item">
        <ol class="rules-sublist">
          <li class="rules-sublist-item">
            Не используйте искусственный интеллект для генерации текстов ваших материалов. Мы не
            против AI и развития технологий, но смысл Philosophy for Us - в создании сообщества
            людей, интересующихся философией и готовых делиться своими знаниями с другими. Поэтому
            для нас важно, чтобы материал писали именно вы и именно для сообщества. Мы рассчитываем,
            что, если человек захочет пообщаться с генеративной нейросейтью о какой-либо философской
            концепции, то он сможет сделать это и без нашей плаформы. Здесь же мы создаём для
            каждого интересующегося пространство для высказывания и знакомства с философией через
            опыт других людей.
          </li>
          <li class="rules-sublist-item">
            Вы можете использовать искусственный интеллект для обсуждения темы, на которую вы пишете
            материал, поиска дополнительных фактов, вычитки итогового текста для улучшения его
            стиля, но не для генерации самого текста.
          </li>
        </ol>
      </li>
